# create a new list
my_list = ["Monday", "Saturday", "Sunday", "Beach", "Coffee"]
print(my_list)

'''A list type of python just like array in other language you can access to items of list using <name_of_list>[number_of_item], the first item has index 0.
Negative indexing means start from the end
-1 refers to the last item, -2 refers to the second last item etc.'''

# example
print(f"Today is {my_list[0]}")
#	negative indexing
print(f"I love {my_list[-1]}")
# both
print(f"On {my_list[0]}, I usually drink a cup of {my_list[-1]} before I go to school")