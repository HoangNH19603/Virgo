#syntax of substring: string[start:end:step]
'''
Example
string[start:end:step]    get all characters from start to end -1 discounting every step character
string[start:end]   get all characters from start to end -1
string[start:]      get all character from index start to the end of string
string[:end]    get all characters from the beginning of the string to end -1
string[:]    get all characters from the beginning of the string to the end of the string
string[::-1]    get all characters in reverse order
'''
# string = "Reverse string"
# print(string[::-1])
print("LeveL"[::-1])
print("LeveL"[1:-1])